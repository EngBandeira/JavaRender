package Basics;

public class Point3D {
    public float x;
    public float y;
    public float z;
    public Point3D(float X, float Y, float Z)
    {
        x=X;
        y=Y;
        z=Z;
    }
    public float getX(){
        return x;
    }
    public float getY(){
        return y;
    }
    public float getZ(){
        return z;
    }
}
